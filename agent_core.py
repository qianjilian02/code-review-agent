import json
import time
from datetime import datetime

class BaseAgent:
    def __init__(self, agent_name, model_type):
        self.agent_name = agent_name
        self.model_type = model_type
        self.task_log = []
        self.status = "idle"

    def log_task(self, task_id, content, result):
        log = {
            "task_id": task_id,
            "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            "content": content,
            "result": result,
            "agent": self.agent_name
        }
        self.task_log.append(log)
        return log

    def get_status(self):
        return {
            "agent": self.agent_name,
            "model": self.model_type,
            "status": self.status,
            "total_tasks": len(self.task_log)
        }

